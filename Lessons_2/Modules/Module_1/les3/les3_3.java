package Lessons_2.Modules.Module_1.les3;

public class les3_3 {
    public static void main(String[] args) {
        byte num = 10;

        switch (num) {
            case 1:
                System.out.println("Переменная равна 1");
                break;
            case 2:
                System.out.println("Переменная равна 2");
                break;
            case 9:
                System.out.println("Переменная равна 9");
                break;
            default:
                System.out.println("Что-то другое");
        }
    }
}
